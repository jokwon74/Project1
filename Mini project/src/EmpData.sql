-- employee
CREATE TABLE EMPLOYEE (
    EMP_ID number GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMP_NAME   varchar2(10) PRIMARY KEY,
    EMP_PHONE  varchar2(20),
    EMP_BIRTH  date,
    EMP_EMAIL  varchar2(50),
    DEPT_CODE  varchar2(10),
    JOB_CODE   varchar2(10),
    HIRE_DATE  date
)

-- 출퇴근
CREATE TABLE COMMUTE (
    EMP_ID        NUMBER REFERENCES EMPLOYEE(EMP_ID),
    STARTTIME     DATE,
    ENDTIME       DATE,
    BUSINESS_TRIP VARCHAR2(5),
    LEAVE         VARCHAR2(5)
)

-- ID/PW
CREATE TABLE LOGIN (
    EMP_ID        NUMBER REFERENCES EMPLOYEE(EMP_ID),
    EMP_PW        VARCHAR2(15)
)

-- 근태 테이블(ATTENDANCE)
CREATE TABLE ATTENDANCE(
LEAVE number,
OVERTIME number,
ANNUALPAIDLEAVE number,
BUSINESS_TRIP number
DATE date,
AVAILABILITY varchar(y/n)
);

-- 직급 테이블(POSITION_RANK)
CREATE TABLE POSITION_RANK(
JOB_CODE varchar(10),
POSITION varchar(10)
);

-- 출장지 테이블(BUSINESS_TRIP_DESTINATION)
CREATE TABLE BUSINESS_TRIP_DESTINATION(
PLACE_CODE varchar(10),
PLACE varchar(10)
);

-- 출장 테이블(BUSINESS_TRIP)
CREATE TABLE BUSINESS_TRIP(
DATE date,
PLACE_CODE varchar(10),
EMP_ID int,
WHETHER not null varchar(10)
);

--부서
CREATE TABLE department (
    dept_id   VARCHAR2(10),
    dept_name VARCHAR2(10)
);
--급여
CREATE TABLE salary(
    EMP_ID NUMBER REFERENCES EMPLOYEE(EMP_ID),
    SALARY NUMBER,
    monthLY_pay NUMBER, 
    BONUS NUMBER
);
--보고
CREATE TABLE REFORT(
    TITLE VARCHAR2(50),
    CONTENT VARCHAR2(100),
    FROM_ID NUMBER REFERENCES EMPLOYEE(EMP_ID),
    TO_ID NUMBER REFERENCES EMPLOYEE(EMP_ID)
);
--공지
CREATE TABLE NOTICE(
    TITLE VARCHAR2(50),
    CONTENT VARCHAR2(100),
    WRITER_ID NUMBER REFERENCES EMPLOYEE(EMP_ID),
    WRITE_DATE DATE
);